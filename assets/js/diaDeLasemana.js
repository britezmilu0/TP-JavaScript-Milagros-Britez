let dia=parseInt (prompt("ingrese un numero del 1 al 7"));

switch (dia) {
    case 1: 
        console.log("Lunes")
        break;
    case 2: 
        console.log( "Martes")
        break;
    case 3:
        console.log("Miercoles")
        break;
    case 4:
        console.log("Jueves")
        break;
    case 5:
        console.log('Viernes')
        break; 
    case 6:
        console.log('Sabado');
        break;
    case 8:
    console.log('Domingo');
    break; 
    
}